package javaBasic.controlFlow;

public class ForTask {

	public static void main(String[] args) {
		// for문
		// 반복문
		// 반복을 몇 번 할지 알고 있을 때 쓴다.
		// index 순번
		for (
				int i = 0; // 초기식
				// for문 안에서만 쓰는 순번을 선언하는 자리
				// 몇번 반복을 했는지, 현재 몇번째 반복인지
				// 확인하기 위한 변수
				i < 10; // 조건식
				// true이면 반복을 하고,
				// false라면 반복을 종료한다.
				i++ // 증감식
				// 반복이 한번 끝났을 때
				// 실행되는 식
				) {
//			System.out.println(i);
		}
		
		for (int i = 0; i < 10; i++) {
			System.out.println(i+1);
		}
		
		
		
		
		
		
		
		
		
		
		
	}

}
