package javaBasic.controlFlow;

public class WhileMain {

	public static void main(String[] args) {
		// 무한히 반복 실행하되 조건에 따라 탈출하는 while문
		int i = 0;
		while(true) {
			System.out.println("반복" + i);
			if(i >= 9) {
				System.out.println("탈출");
				break;
			}
			i++;
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
